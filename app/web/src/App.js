import React, {useState} from 'react';


//Component: Um bloco isolado de HTML, CSS e JS que não interfere no restante da aplicação
  //Semelhante aos components do unity
  //Nome começa com letra Maiuscula
  //So deve existir um componente por arquivo

//Propriedade : Informações que um Parent passa para os chidrens

//Estado : Informações mantidas pelo componente em run time (Lembrar : Imutabilidade)


//Importação do Component Header
import Header from './Header';

//Componente Chamado App
function App() {
  const [counter, setCounter] = useState(0);

  function incrementCounter(){
    setCounter(counter + 1);
  }
  
  return (
    <>
      <h1>Contador: {counter}</h1>
      <button onClick = {incrementCounter}>Increment</button>
    </>
    
    
    
    /*
    //Quando se usam varios componentes no mesmo return 
    //é necessario colocar um container neles
    //o container usado é o fragment (<> ... </>) que não aparece no HTML final
    <>
    <Header title="Text generated By props" />
    <Header title="Text generated By props text 2" />
    <Header title="Text generated By props, I can have multiple Components"/>
    </>
    */
    
  );
}

export default App;
